    document.addEventListener('DOMContentLoaded', function() {
        // Initialize tooltips
        var tooltipTriggerList = [].slice.call(document.querySelectorAll('[data-bs-toggle="tooltip"]'));
        var tooltipList = tooltipTriggerList.map(function (tooltipTriggerEl) {
            return new bootstrap.Tooltip(tooltipTriggerEl);
        });
        
        // Add staggered animation delay to each product card
        const productCards = document.querySelectorAll('.featured-imagebox-portfolio.style3');
        productCards.forEach((card, index) => {
            card.style.animationDelay = `${index * 0.1}s`;
            card.style.transitionDelay = `${index * 0.05}s`;
        });
        
        // Filter functionality
        const filterButtons = document.querySelectorAll('.filter-btn');
        const portfolioItems = document.querySelectorAll('.portfolio-item');
        
        filterButtons.forEach(button => {
            button.addEventListener('click', function() {
                // Update active state
                filterButtons.forEach(btn => btn.classList.remove('active'));
                this.classList.add('active');
                
                const filterValue = this.getAttribute('data-filter');
                
                portfolioItems.forEach(item => {
                    if (filterValue === 'all' || item.getAttribute('data-category').includes(filterValue)) {
                        item.classList.remove('hide');
                    } else {
                        item.classList.add('hide');
                    }
                });
                
                // Reapply animations to visible items
                setTimeout(() => {
                    const visibleItems = document.querySelectorAll('.portfolio-item:not(.hide)');
                    visibleItems.forEach((item, index) => {
                        item.style.animation = 'none';
                        void item.offsetWidth; // Trigger reflow
                        item.style.animation = `slideUp 0.6s ease forwards ${index * 0.1}s`;
                    });
                }, 50);
            });
        });

        // Add hover effect to product cards
        productCards.forEach(card => {
            const exploreBtn = card.querySelector('.explore-btn');
            
            card.addEventListener('mouseenter', () => {
                exploreBtn.style.transform = 'translateX(5px)';
            });
            
            card.addEventListener('mouseleave', () => {
                exploreBtn.style.transform = 'translateX(0)';
            });
        });

        // Add smooth scroll to all links
        document.querySelectorAll('a[href^="#"]').forEach(anchor => {
            anchor.addEventListener('click', function(e) {
                e.preventDefault();
                document.querySelector(this.getAttribute('href')).scrollIntoView({
                    behavior: 'smooth'
                });
            });
        });

        // Lazy loading for images
        if ('IntersectionObserver' in window) {
            const lazyImageObserver = new IntersectionObserver((entries) => {
                entries.forEach((entry) => {
                    if (entry.isIntersecting) {
                        const lazyImage = entry.target;
                        lazyImage.src = lazyImage.dataset.src;
                        lazyImage.classList.remove('lazy');
                        lazyImageObserver.unobserve(lazyImage);
                    }
                });
            });

            document.querySelectorAll('img.lazy').forEach((img) => {
                lazyImageObserver.observe(img);
            });
        }
    });

